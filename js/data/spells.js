export const SPELL_IDS = Object.keys(SPELLS);

export function spellsForClass(classId) {
  return Object.values(SPELLS).filter(s => s.classes?.includes(classId));
}

export function spellsByLevel(classId) {
  const map = new Map();
  for (const s of spellsForClass(classId)) {
    if (!map.has(s.level)) map.set(s.level, []);
    map.get(s.level).push(s);
  }
  for (const list of map.values()) list.sort((a,b) => a.name.localeCompare(b.name));
  return map;
}
